            <h1 class="mb-4 text-center">Privacy Policy</h1>

            <p>
                <strong>Beauty of Morocco – INCI Literacy App</strong> is an educational mobile application designed to
                help users understand cosmetic ingredients using the International Nomenclature of Cosmetic Ingredients (INCI).
                This Privacy Policy explains how information is handled when using the application.
            </p>

            <hr>

            <h4>Purpose of the App</h4>
            <p>
                Beauty of Morocco is built as an ingredient literacy tool for women and girls in Morocco.
                The application allows users to scan cosmetic barcodes and view ingredient lists with
                simple, educational health markers.
            </p>
            <p>
                The app is intended for educational purposes only and does not provide medical advice,
                safety ratings, or product recommendations.
            </p>

            <hr>

            <h4>Information We Do Not Collect</h4>
            <p>
                Beauty of Morocco follows a privacy-first approach. We do not collect, store, or process:
            </p>
            <ul>
                <li>Personal information (name, email, phone number)</li>
                <li>User accounts or login credentials</li>
                <li>Location data</li>
                <li>Payment or financial information</li>
                <li>Health or medical data</li>
            </ul>
            <p>
                The application can be used completely anonymously.
            </p>

            <hr>

            <h4>Barcode & Product Information</h4>
            <p>
                When a user scans a product barcode, the barcode is used only to retrieve product and
                ingredient information from a public database.
            </p>
            <p>
                Product and ingredient data is fetched from the
                <strong>Open Beauty Facts</strong> API.
            </p>

            <hr>

            <h4>Ingredient Information & Markers</h4>
            <p>
                Ingredient explanations and markers are based on public scientific and regulatory sources,
                including EU CosIng and PubChem, and a locally maintained ingredient knowledge base.
            </p>

            <p>
                Ingredient markers may include:
            </p>
            <ul>
                <li>Preservative</li>
                <li>Fragrance / Allergen</li>
                <li>Potential Irritant</li>
                <li>Potential Endocrine Disruptor (EDC)</li>
                <li>Microplastic</li>
                <li>Pregnancy Caution</li>
            </ul>

            <p>
                These markers are provided for educational purposes only and do not evaluate product safety,
                quality, or suitability.
            </p>

            <hr>

            <h4>Missing Product Submissions</h4>
            <p>
                If a product is not found, users may optionally submit a barcode and photos of the product
                label and ingredient panel.
            </p>
            <p>
                Submitted information is used solely for moderation and database improvement and is not
                linked to any personal identity.
            </p>

            <hr>

            <h4>Offline Data Storage</h4>
            <p>
                The application may store limited data locally on the device, such as recently scanned
                products and a local ingredient knowledge base, to improve performance and allow limited
                offline access.
            </p>

            <hr>

            <h4>App Permissions</h4>
            <p>
                The application may request access to:
            </p>
            <ul>
                <li><strong>Camera</strong> – for barcode scanning and photo submission</li>
                <li><strong>Storage</strong> – for uploading product images</li>
            </ul>
            <p>
                These permissions are used strictly for core application functionality.
            </p>

            <hr>

            <h4>Children’s Privacy</h4>
            <p>
                Beauty of Morocco does not knowingly collect data from children. As no personal data is
                collected, no user profiling occurs.
            </p>

            <hr>

            <h4>Policy Updates</h4>
            <p>
                This Privacy Policy may be updated periodically. Any changes will be communicated within
                the application or through official channels.
            </p>

            <hr>

        </div>
